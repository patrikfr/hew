(ns hew.tempmap)

(defn to-color
  "Gives the corresponding RGB color for given temperature reading"
  [temp]
  (condp #(and (<= (%1 0) %2) (< %2 (%1 1))) temp
    [-35 -25] [81, 55, 176]
    [-25 -20] [50, 142, 176]
    [-20 -15] [59, 171, 213]
    [-15 -10] [122, 193, 227]
    [-10 -5] [161, 218, 248]
    [-5 0] [193, 235, 251]
    [0 5] [194, 221, 176]
    [5 10] [222, 221, 38]
    [10 15] [255, 229, 0]
    [15 20] [248, 165, 0]
    [20 25] [237, 107, 6]
    [25 30] [227, 6, 19]
    [30 40] [204, 23, 25]
    [255, 255, 240]))                                       ;Default: ivory


